"Contiene los métodos de suma y resta"


# Function to add two numbers
def add(a, b):
    """Método de suma de dos valores"""
    # Return the sum of a and b
    return a + b


# Function to subtract one number from another
def subtract(a, b):
    """Método de resta de dos valores"""
    # Return the result of a minus b
    return a - b
